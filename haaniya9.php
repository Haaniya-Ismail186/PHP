<?php
// Decrement aur Assignment ka naya code
echo "<h1>Decrement</h1>";

$number = 50; // Shuruati value 50

echo $number--; // Output: 50 (Post-decrement: pehle print phir minus)
echo "<br>";

echo $number;   // Output: 49 (Ab value 49 ho chuki hai)
echo "<br>";

echo --$number; // Output: 48 (Pre-decrement: pehle minus phir print)
echo "<br>";

// Subtract Assignment Operator
$number -= 15;  // 48 mein se 15 nikaal diye (48 - 15 = 33)
echo $number;   // Output: 33
?>